#include "3-calc.h"

/**
 * op_add - adds sum of a and b
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return ((a) + (b));
}
/**
 * op_sub - subtraction
 * @a: first digit
 * @b:second digit
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_mul - multiplies
 * @a: first no
 * @b: second Nmber
 * Return: product
 */
int op_mul(int a, int b)
{
	return ((a) * (b));
}
/**
 * op_div - division
 * @a: first number
 * @b: second number
 * Return: dividion result
 */
int op_div(int a, int b)
{
	return ((a) / (b));
}
/**
 * op_mod - remainder
 * @a: first number
 * @b: second number
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return ((a) % (b));
}
